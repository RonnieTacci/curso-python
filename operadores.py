#Operadores matematicos

#Operadores basicos: + , - , *, / , % , **
# % -> resto de divisão.
# **-> numero elevado.(potencia)

#Operadores de comparação: == , != , > , < , >= , <=

print(5 % 2) #resto 1 - numero impar
print(4 ** 2) 

print( 2 == 3) #(==) -> igual - False
print( 2 != 3) #(!=) -> diferente - True